public class NumberSum {
    public static void main(String[] args) {
        int sum = 0;

        // Calculate the sum of numbers from 1 to 10
        for (int i = 1; i <= 10; i++) {
            sum += i;
        }

        // Print the output
        System.out.println("The sum of numbers from 1 to 10 is: " + sum);
    }
}
